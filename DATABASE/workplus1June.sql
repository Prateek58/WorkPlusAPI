CREATE ALGORITHM=UNDEFINED SQL SECURITY DEFINER VIEW `hr_vw_current_month_attendance` AS SELECT 
    w.worker_id,
    w.full_name as worker_name,
    w.type_id,
    COUNT(CASE WHEN ha.status = 'Present' THEN 1 END) as present_days,
    COUNT(CASE WHEN ha.status = 'Absent' THEN 1 END) as absent_days,
    COUNT(CASE WHEN ha.status = 'Half Day' THEN 1 END) as half_days,
    COUNT(CASE WHEN ha.status = 'Leave' THEN 1 END) as leave_days,
    COUNT(CASE WHEN ha.status = 'CompOff' THEN 1 END) as comp_off_days,
    COUNT(*) as total_marked_days
FROM workers w
LEFT JOIN hr_attendance ha ON w.worker_id = ha.worker_id 
    AND MONTH(ha.attendance_date) = MONTH(CURRENT_DATE())
    AND YEAR(ha.attendance_date) = YEAR(CURRENT_DATE())
WHERE w.is_active = TRUE
GROUP BY w.worker_id, w.full_name, w.type_id 
;

